// בחירת הניסוח הנכון לפי מגדר, עם נפילה אחורה לטקסט ישן אם קיים.
export function genderLabel(item, gender) {
  if (!item) return "";
  const g = gender === "female" ? "female" : "male";
  return item[g] || item.male || item.female || item.label || "";
}

